# pip install paddlenlp paddlepaddle
from paddlenlp import Taskflow
# 人物介绍专用 schema（官网混合格式）
schema = [
    "姓名",
    "性别",
    "出生日期",
    "出生地",
    "民族",
    "国籍",
    "政治面貌",
    "学历",
    "学位",
    "毕业院校",
    "职务",
    "职称",
    "工作单位",
    "荣誉称号",
    "研究领域",
    "联系方式",
    "电子邮箱",
    {
        "人物": ["曾任职单位", "现任职务", "主要成就", "社会兼职", "家庭成员"],
        "工作经历": ["开始时间", "结束时间", "单位名称", "担任职务"],
        "教育经历": ["入学时间", "毕业时间", "学校名称", "专业", "学历"],
        "荣誉奖项": ["获得时间", "奖项名称", "颁发单位"]
    }
]

uie = Taskflow(
    "information_extraction",
    model="uie-base",
    schema=schema,
    is_static=False,  # 2.6.1 支持！Windows 必加
    device_id=-1
)

# 测试人物介绍文本
text = """
张三，男，1985年3月15日出生于北京市，汉族，中国国籍，中共党员。
博士研究生学历，理学博士学位，毕业于清华大学计算机科学与技术专业。
现任北京大学人工智能研究院教授、博士生导师，主要研究方向为自然语言处理、机器学习。
2010年9月至2017年7月，任职于百度研究院，担任高级研究员。
曾获国家科技进步二等奖、北京市杰出青年科学家奖，兼任中国人工智能学会理事。
联系方式：13800138000，邮箱：zhangsan@pku.edu.cn。
"""

# 抽取结果（实体+关系同时输出）
result = uie(text)

# 打印查看
import pprint
pprint.pprint(result)




